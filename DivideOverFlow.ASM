ASSUME CS:CODE

CODE SEGMENT

START:
MOV AX,CS
MOV DS,AX
MOV SI,OFFSET DO;设置DS:SI指向源地址

MOV AX,0
MOV ES,AX
MOV DI,200H;设置ES:DI指向目标地址

MOV CX,OFFSET DOEND-OFFSET DO;设置CX为传输长度
CLD
REP MOVSB

MOV WORD PTR ES:[0],200H;DO的入口偏移地址
MOV WORD PTR ES:[2],0;DO的入口段地址

MOV DX,0FFFFH
MOV BX,1
DIV BX;除法测试

MOV AX,4C00H
INT 21H

DO:JMP SHORT DOSTART
DB 'divide error!'
DOSTART:
MOV AX,0
MOV DS,AX
MOV SI,202H;设置DS:SI指向字符串

MOV AX,0B800H
MOV ES,AX
MOV DI,160*12+2*33;设置ES:DI指向显存空间的中间位置

MOV CX,13;设置CX为字符串长度
S:
MOV AL,DS:[SI]
MOV AH,2
MOV ES:[DI],AX
INC SI
INC DI
INC DI
LOOP S;逐个打印字符

MOV AX,4C00H
INT 21H

DOEND:NOP

CODE ENDS

END START